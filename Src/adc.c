uint16_t adcFilter(uint16_t *adcV)
{
    uint16_t minV = 0xffff, maxV = 0, sumV = 0;
    uint8_t i;
    for (i = 0; i < 10; i++)
    {
        sumV += adcV[i];
        minV = (minV > adcV[i] ? adcV[i] : minV);
        maxV = (maxV < adcV[i] ? adcV[i] : maxV);
    }
    return ((sumV - minV - maxV) >> 3);
}

